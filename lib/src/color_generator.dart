import 'dart:math';

import 'package:flutter/material.dart';

class ColorGenerator {
  ColorGenerator._internal();
  static ColorGenerator _instance = ColorGenerator._internal();
  factory ColorGenerator() => _instance;

  Random random = Random();

  static final _materialColors = <Color>[
    Color(0xffe57373),
    Color(0xfff06292),
    Color(0xffba68c8),
    Color(0xff9575cd),
    Color(0xff7986cb),
    Color(0xff64b5f6),
    Color(0xff4fc3f7),
    Color(0xff4dd0e1),
    Color(0xff4db6ac),
    Color(0xff81c784),
    Color(0xffaed581),
    Color(0xffff8a65),
    Color(0xffd4e157),
    Color(0xffffd54f),
    Color(0xffffb74d),
    Color(0xffa1887f),
    Color(0xff90a4ae)
  ];

  Color getRandomColor() =>
      _materialColors[random.nextInt(_materialColors.length)];
}
